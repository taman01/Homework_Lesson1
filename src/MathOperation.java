public class MathOperation {
    public static void main(String[] args) {
        // Инициализируем переменные типа int
        int a = 12;
        int b = 5;
        System.out.println("Сумма чисел = " + (a + b));
        System.out.println("Разность чисел = " + (a - b));
        System.out.println("Произведение чисел = " + (a * b));
        System.out.println("Частное чисел = " + (a / b)); //
        // Инициализируем переменные типа double
        double c = 12.5;
        double d = 5;
        System.out.println("-------------------------------");
        System.out.println("Сумма чисел = " + (c + d));
        System.out.println("Разность чисел = " + (c - d));
        System.out.println("Произведение чисел = " + (c * d));
        System.out.println("Частное чисел = " + (c / d));


    }
}
